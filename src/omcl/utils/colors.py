import numpy as np

import matplotlib.pyplot as plt

def generate_rgb_colors(n):
    """
    Generate `n` visually distinct RGB colors as a numpy array in [0, 255].
    
    Parameters:
        n (int): Number of RGB colors to generate.
        
    Returns:
        numpy.ndarray: Array of shape (n, 3), with RGB values in [0, 255].
    """
    cmap = plt.get_cmap('hsv')  # You can change this to 'tab20', 'nipy_spectral', etc.
    colors = [cmap(i / n)[:3] for i in range(n)]  # drop alpha
    colors_255 = (np.array(colors) * 255).astype(np.uint8)
    np.random.shuffle(colors_255)
    np.random.shuffle(colors_255)
    return colors_255



